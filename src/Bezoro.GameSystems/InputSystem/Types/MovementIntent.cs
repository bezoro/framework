namespace Bezoro.GameSystems.InputSystem.Types;

/// <summary>
///     Current movement intent applied to an entity.
/// </summary>
public struct MovementIntent
{
	/// <summary>
	///     Gets or sets the intent X movement component.
	/// </summary>
	public float X;

	/// <summary>
	///     Gets or sets the intent Y movement component.
	/// </summary>
	public float Y;

	/// <summary>
	///     Gets or sets the intent Z movement component.
	/// </summary>
	public float Z;
}
